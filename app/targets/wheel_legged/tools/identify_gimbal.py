"""
Hero 云台动力学参数辨识工具

辨识 9 个参数 (theta0-theta8)：
  theta0/theta1 = 耦合惯量 (I1zz_com, I2xx_com)
  theta2        = Pitch 惯量 (I2yy_com)
  theta3/theta4 = 重力项 (m2*l2x, m2*l2z)
  theta5/theta6 = Yaw 摩擦 (fv1, fc1)
  theta7/theta8 = Pitch 摩擦 (fv2, fc2)

用法:
  python identify_gimbal.py gravity  gravity_static.csv
  python identify_gimbal.py friction friction_sweep.csv
  python identify_gimbal.py pitch    dynamic_pitch.csv
  python identify_gimbal.py coupling dynamic_yaw_q2_*.csv
  python identify_gimbal.py verify   dynamic_harmonics.csv
  python identify_gimbal.py guide
"""

