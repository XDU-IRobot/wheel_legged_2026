# ─────────────────────────── CLI ───────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Hero 云台动力学参数辨识工具",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  %(prog)s guide                          显示操作指导
  %(prog)s gravity  gravity_static.csv    重力项辨识
  %(prog)s friction friction_sweep.csv    摩擦项辨识
  %(prog)s pitch    dynamic_pitch.csv     Pitch 惯量辨识
  %(prog)s coupling yaw_q2_*.csv          耦合惯量辨识
  %(prog)s verify   harmonics.csv         五次谐波验证
""",
    )
    parser.add_argument("mode", choices=["guide", "gravity", "friction", "pitch", "coupling", "verify"],
                        help="辨识阶段")
    parser.add_argument("files", nargs="*", help="输入 CSV 文件")
    parser.add_argument("--output", "-o", help="输出 theta 文件")
    parser.add_argument("--gravity", nargs=2, type=float, metavar=("THETA3", "THETA4"),
                        help="已知重力参数 (用于摩擦/惯量/耦合辨识)")
    parser.add_argument("--friction", nargs=4, type=float, metavar=("T5", "T6", "T7", "T8"),
                        help="已知摩擦参数 (用于惯量/耦合辨识)")

    args = parser.parse_args()

    if args.mode == "guide":
        print_guide()
        return

    if not args.files:
        print("[ERROR] 需要提供 CSV 文件")
        parser.print_help()
        sys.exit(1)

    if args.mode == "gravity":
        df = load_csv(args.files[0])
        theta = identify_gravity(df)
        if args.output:
            save_theta(theta, args.output)

    elif args.mode == "friction":
        df = load_csv(args.files[0])
        if args.gravity:
            theta3, theta4 = args.gravity
        else:
            print("[INFO] 未提供重力参数，使用 theta3=0, theta4=0")
            theta3, theta4 = 0.0, 0.0
        theta = identify_friction(df, theta3, theta4)
        if args.output:
            save_theta(theta, args.output)

    elif args.mode == "pitch":
        df = load_csv(args.files[0])
        if args.gravity:
            theta3, theta4 = args.gravity
        else:
            theta3, theta4 = 0.0, 0.0
        if args.friction:
            theta7, theta8 = args.friction[2], args.friction[3]
        else:
            theta7, theta8 = 0.0, 0.0
        theta = identify_pitch_inertia(df, theta3, theta4, theta7, theta8)
        if args.output:
            save_theta(theta, args.output)

    elif args.mode == "coupling":
        if args.friction:
            theta5, theta6 = args.friction[0], args.friction[1]
        else:
            theta5, theta6 = 0.0, 0.0
        theta = identify_coupling(args.files, theta5, theta6)
        if args.output:
            save_theta(theta, args.output)

    elif args.mode == "verify":
        df = load_csv(args.files[0])
        if args.gravity and args.friction:
            theta = np.zeros(9)
            theta[3], theta[4] = args.gravity
            theta[5], theta[6], theta[7], theta[8] = args.friction
        else:
            print("[INFO] 未提供完整参数，使用全零 theta")
            theta = np.zeros(9)
        verify_harmonics(df, theta)


if __name__ == "__main__":
    main()
