G = 9.81  # m/s^2


# ─────────────────────────── 数据加载 ───────────────────────────

def load_csv(path: str) -> pd.DataFrame:
    """加载辨识 CSV，列: time, q1, q2, dq1, dq2, tau1, tau2"""
    df = pd.read_csv(path)
    required = {"time", "q1", "q2", "dq1", "dq2", "tau1", "tau2"}
    if not required.issubset(set(df.columns)):
        missing = required - set(df.columns)
        print(f"[ERROR] CSV 缺少列: {missing}")
        print(f"  当前列: {list(df.columns)}")
        sys.exit(1)
    # 转为 float
    for c in df.columns:
        df[c] = df[c].astype(float)
    print(f"  已加载 {len(df)} 行, 时间范围 [{df['time'].iloc[0]:.3f}, {df['time'].iloc[-1]:.3f}] s")
    return df


def save_theta(theta: np.ndarray, output: str = None):
    """打印 theta 并可选保存到文件"""
    print("\n" + "=" * 50)
    print("辨识结果 (kIdentTheta[9]):")
    print("=" * 50)
    labels = [
        "theta0: I1zz_com  (yaw 耦合惯量)",
        "theta1: I2xx_com  (yaw 耦合惯量)",
        "theta2: I2yy_com  (pitch 惯量)",
        "theta3: m2*l2x    (重力水平偏心)",
        "theta4: m2*l2z    (重力垂直偏心)",
        "theta5: fv1       (yaw 粘滞摩擦)",
        "theta6: fc1       (yaw 库仑摩擦)",
        "theta7: fv2       (pitch 粘滞摩擦)",
        "theta8: fc2       (pitch 库仑摩擦)",
    ]
    for i, (v, lbl) in enumerate(zip(theta, labels)):
        print(f"  [{i}] {v:12.6f}  {lbl}")

    # C++ 数组格式
    cpp_lines = []
    for i, (v, lbl) in enumerate(zip(theta, labels)):
        comma = "," if i < 8 else ""
        cpp_lines.append(f"    {v:.6f}f{comma}  // {lbl}")
    cpp_block = "constexpr float kIdentTheta[9] = {\n" + "\n".join(cpp_lines) + "\n};"
    print("\n── 可复制到 params.hpp ──")
    print(cpp_block)

    if output:
        with open(output, "w") as f:
            f.write("// Auto-generated by identify_gimbal.py\n")
            f.write(cpp_block + "\n")
        print(f"\n已保存到 {output}")


# ─────────────────────────── Step 1: 重力辨识 ───────────────────────────

def identify_gravity(df: pd.DataFrame):
    """
    辨识 theta3 (m2*l2x) 和 theta4 (m2*l2z)

    模型: tau2 = -g * (theta3*cos(q2) + theta4*sin(q2)) + C
    即:   tau2 = A*cos(q2) + B*sin(q2) + C
    所以:  theta3 = -A/g, theta4 = -B/g
    """
    print("\n" + "=" * 50)
    print("Step 1: 重力项辨识 (theta3, theta4)")
    print("=" * 50)

    # 筛选静态数据: 速度接近零
    vel_thresh = 0.02  # rad/s
    mask = (df["dq1"].abs() < vel_thresh) & (df["dq2"].abs() < vel_thresh)
    df_static = df[mask].copy()
    print(f"  静态数据点: {len(df_static)} / {len(df)}")

    if len(df_static) < 10:
        print("  [WARN] 静态数据太少，使用全部数据（假设近似静态）")
        df_static = df

    # 按角度分箱取平均，减少噪声
    q2_bins = np.linspace(df_static["q2"].min(), df_static["q2"].max(), 20)
    df_static = df_static.copy()
    df_static["q2_bin"] = np.digitize(df_static["q2"], q2_bins)
    grouped = df_static.groupby("q2_bin").agg({"q2": "mean", "tau2": "mean", "dq2": "mean"}).dropna()

    q2 = grouped["q2"].values
    tau2 = grouped["tau2"].values

    # 线性最小二乘: tau2 = A*cos(q2) + B*sin(q2) + C
    Y = np.column_stack([np.cos(q2), np.sin(q2), np.ones_like(q2)])
    result, residuals, rank, sv = np.linalg.lstsq(Y, tau2, rcond=None)
    A, B, C = result

    theta3 = -A / G
    theta4 = -B / G

    # R²
    tau2_pred = Y @ result
    ss_res = np.sum((tau2 - tau2_pred) ** 2)
    ss_tot = np.sum((tau2 - np.mean(tau2)) ** 2)
    r2 = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f"\n  拟合结果:")
    print(f"    A = {A:.6f}, B = {B:.6f}, C = {C:.6f}")
    print(f"    theta3 (m2*l2x) = {theta3:.6f}")
    print(f"    theta4 (m2*l2z) = {theta4:.6f}")
    print(f"    常值偏置 C = {C:.6f}  {'(正常)' if abs(C) < 1.0 else '(偏大，检查零偏)'}")
    print(f"    R² = {r2:.6f}")

    theta = np.zeros(9)
    theta[3] = theta3
    theta[4] = theta4
    save_theta(theta)
    return theta


# ─────────────────────────── Step 2: 摩擦辨识 ───────────────────────────

def identify_friction(df: pd.DataFrame, theta3: float, theta4: float):
    """
    辨识 theta5/theta6 (yaw 摩擦) 和 theta7/theta8 (pitch 摩擦)

    Yaw:  tau1 = theta5*dq1 + theta6*tanh(dq1)
    Pitch: tau2_no_grav = theta7*dq2 + theta8*tanh(dq2)
    """
    print("\n" + "=" * 50)
    print("Step 2: 摩擦项辨识 (theta5, theta6, theta7, theta8)")
    print("=" * 50)

    theta = np.zeros(9)
    theta[3] = theta3
    theta[4] = theta4

    # ── Yaw 摩擦 ──
    print("\n  [Yaw 摩擦]")
    # 筛选匀速段: 加速度小
    v_eps = 0.02  # 最小速度阈值
    a_thresh = 0.5  # rad/s^2
    # 用差分近似加速度
    dq1_diff = np.gradient(df["dq1"], df["time"])
    mask_yaw = (df["dq1"].abs() > v_eps) & (np.abs(dq1_diff) < a_thresh)
    df_yaw = df[mask_yaw].copy()
    print(f"    匀速数据点: {len(df_yaw)} / {len(df)}")

    if len(df_yaw) < 10:
        print("    [WARN] 匀速数据太少，使用全部数据")
        df_yaw = df

    dq1 = df_yaw["dq1"].values
    tau1 = df_yaw["tau1"].values

    # tau1 = fv1*dq1 + fc1*tanh(dq1)
    Y_yaw = np.column_stack([dq1, np.tanh(dq1)])
    result_yaw, _, _, _ = np.linalg.lstsq(Y_yaw, tau1, rcond=None)
    theta5, theta6 = result_yaw

    tau1_pred = Y_yaw @ result_yaw
    ss_res = np.sum((tau1 - tau1_pred) ** 2)
    ss_tot = np.sum((tau1 - np.mean(tau1)) ** 2)
    r2_yaw = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f"    theta5 (fv1) = {theta5:.6f}")
    print(f"    theta6 (fc1) = {theta6:.6f}")
    print(f"    R² = {r2_yaw:.6f}")

    # ── Pitch 摩擦 ──
    print("\n  [Pitch 摩擦]")
    # 扣除重力
    tau_gravity = -G * (theta3 * np.cos(df["q2"].values) + theta4 * np.sin(df["q2"].values))
    tau2_no_grav = df["tau2"].values - tau_gravity

    # 筛选匀速段
    dq2_diff = np.gradient(df["dq2"], df["time"])
    mask_pitch = (df["dq2"].abs() > v_eps) & (np.abs(dq2_diff) < a_thresh)
    df_pitch = df[mask_pitch].copy()
    tau2_ng_pitch = tau2_no_grav[mask_pitch]
    print(f"    匀速数据点: {len(df_pitch)} / {len(df)}")

    if len(df_pitch) < 10:
        print("    [WARN] 匀速数据太少，使用全部数据")
        df_pitch = df
        tau2_ng_pitch = tau2_no_grav

    dq2 = df_pitch["dq2"].values

    # tau2_no_grav = fv2*dq2 + fc2*tanh(dq2)
    Y_pitch = np.column_stack([dq2, np.tanh(dq2)])
    result_pitch, _, _, _ = np.linalg.lstsq(Y_pitch, tau2_ng_pitch, rcond=None)
    theta7, theta8 = result_pitch

    tau2_pred = Y_pitch @ result_pitch
    ss_res = np.sum((tau2_ng_pitch - tau2_pred) ** 2)
    ss_tot = np.sum((tau2_ng_pitch - np.mean(tau2_ng_pitch)) ** 2)
    r2_pitch = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f"    theta7 (fv2) = {theta7:.6f}")
    print(f"    theta8 (fc2) = {theta8:.6f}")
    print(f"    R² = {r2_pitch:.6f}")

    theta[5] = theta5
    theta[6] = theta6
    theta[7] = theta7
    theta[8] = theta8
    save_theta(theta)
    return theta


# ─────────────────────────── Step 3: Pitch 惯量辨识 ───────────────────────────

def identify_pitch_inertia(df: pd.DataFrame, theta3: float, theta4: float,
                           theta7: float, theta8: float):
    """
    辨识 theta2 (I2yy_com)

    tau2_residual = theta2 * ddq2
    其中 tau2_residual = tau2 - tau2_gravity - tau2_friction
    """
    print("\n" + "=" * 50)
    print("Step 3: Pitch 惯量辨识 (theta2)")
    print("=" * 50)

    # 计算 ddq2 (用 Savitzky-Golay 滤波)
    dt = np.mean(np.diff(df["time"]))
    dq2 = df["dq2"].values
    # Savitzky-Golay 滤波求导
    window = min(51, len(dq2) // 4 * 2 + 1)  # 奇数窗口
    if window < 5:
        window = 5
    ddq2 = savgol_filter(dq2, window, 3, deriv=1, delta=dt)

    # 扣除重力
    q2 = df["q2"].values
    tau2 = df["tau2"].values
    tau_gravity = -G * (theta3 * np.cos(q2) + theta4 * np.sin(q2))

    # 扣除摩擦
    tau_friction = theta7 * dq2 + theta8 * np.tanh(dq2)

    tau2_residual = tau2 - tau_gravity - tau_friction

    # 筛选有效数据: ddq 不能太小
    ddq_thresh = 0.1  # rad/s^2
    mask = np.abs(ddq2) > ddq_thresh
    ddq2_valid = ddq2[mask]
    tau2_res_valid = tau2_residual[mask]
    print(f"  有效数据点: {np.sum(mask)} / {len(df)}")

    if np.sum(mask) < 10:
        print("  [WARN] 有效数据太少，使用全部数据")
        ddq2_valid = ddq2
        tau2_res_valid = tau2_residual

    # 最小二乘: tau2_residual = theta2 * ddq2
    result = np.linalg.lstsq(ddq2_valid.reshape(-1, 1), tau2_res_valid, rcond=None)
    theta2 = result[0][0]

    tau2_pred = ddq2_valid * theta2
    ss_res = np.sum((tau2_res_valid - tau2_pred) ** 2)
    ss_tot = np.sum((tau2_res_valid - np.mean(tau2_res_valid)) ** 2)
    r2 = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f"  theta2 (I2yy_com) = {theta2:.6f}")
    print(f"  R² = {r2:.6f}")
    if theta2 < 0:
        print("  [WARN] 惯量为负，检查数据质量")

    theta = np.zeros(9)
    theta[2] = theta2
    theta[3] = theta3
    theta[4] = theta4
    theta[5] = 0  # 不在此步辨识
    theta[6] = 0
    theta[7] = theta7
    theta[8] = theta8
    save_theta(theta)
    return theta


# ─────────────────────────── Step 4: 耦合惯量辨识 ───────────────────────────

def identify_coupling(files: list, theta5: float, theta6: float):
    """
    辨识 theta0/theta1 (I1zz_com, I2xx_com)

    在多个固定 q2 角度下:
    tau1_residual = (theta0*sin²(q2) + theta1*cos²(q2)) * ddq1
    """
    print("\n" + "=" * 50)
    print("Step 4: Yaw-Pitch 耦合惯量辨识 (theta0, theta1)")
    print("=" * 50)

    all_ddq1 = []
    all_tau1_res = []
    all_sin2 = []
    all_cos2 = []

    for f in files:
        print(f"\n  处理: {f}")
        df = load_csv(f)

        dt = np.mean(np.diff(df["time"]))
        dq1 = df["dq1"].values
        window = min(51, len(dq1) // 4 * 2 + 1)
        if window < 5:
            window = 5
        ddq1 = savgol_filter(dq1, window, 3, deriv=1, delta=dt)

        # 扣除 yaw 摩擦
        tau_friction = theta5 * dq1 + theta6 * np.tanh(dq1)
        tau1_residual = df["tau1"].values - tau_friction

        # q2 取平均值（固定 pitch 角度）
        q2_mean = np.mean(df["q2"].values)
        sin2 = np.sin(q2_mean) ** 2
        cos2 = np.cos(q2_mean) ** 2

        # 筛选有效数据
        ddq_thresh = 0.1
        mask = np.abs(ddq1) > ddq_thresh
        print(f"    q2 = {np.degrees(q2_mean):.1f}°, 有效点: {np.sum(mask)} / {len(df)}")

        all_ddq1.append(ddq1[mask])
        all_tau1_res.append(tau1_residual[mask])
        all_sin2.append(np.full(np.sum(mask), sin2))
        all_cos2.append(np.full(np.sum(mask), cos2))

    ddq1_all = np.concatenate(all_ddq1)
    tau1_res_all = np.concatenate(all_tau1_res)
    sin2_all = np.concatenate(all_sin2)
    cos2_all = np.concatenate(all_cos2)

    # 最小二乘: tau1_residual = theta0*sin²(q2)*ddq1 + theta1*cos²(q2)*ddq1
    Y = np.column_stack([sin2_all * ddq1_all, cos2_all * ddq1_all])
    result, _, _, _ = np.linalg.lstsq(Y, tau1_res_all, rcond=None)
    theta0, theta1 = result

    tau1_pred = Y @ result
    ss_res = np.sum((tau1_res_all - tau1_pred) ** 2)
    ss_tot = np.sum((tau1_res_all - np.mean(tau1_res_all)) ** 2)
    r2 = 1 - ss_res / ss_tot if ss_tot > 0 else 0

    print(f"\n  theta0 (I1zz_com) = {theta0:.6f}")
    print(f"  theta1 (I2xx_com) = {theta1:.6f}")
    print(f"  R² = {r2:.6f}")

    theta = np.zeros(9)
    theta[0] = theta0
    theta[1] = theta1
    theta[5] = theta5
    theta[6] = theta6
    save_theta(theta)
    return theta


# ─────────────────────────── Step 5: 五次谐波验证 ───────────────────────────

def verify_harmonics(df: pd.DataFrame, theta: np.ndarray):
    """
    五次谐波综合验证

    比较模型预测力矩与实际力矩，计算跟踪误差
    """
    print("\n" + "=" * 50)
    print("Step 5: 五次谐波综合验证")
    print("=" * 50)

    q1 = df["q1"].values
    q2 = df["q2"].values
    dq1 = df["dq1"].values
    dq2 = df["dq2"].values
    tau1 = df["tau1"].values
    tau2 = df["tau2"].values

    # 计算 ddq
    dt = np.mean(np.diff(df["time"]))
    window = min(51, len(dq1) // 4 * 2 + 1)
    if window < 5:
        window = 5
    ddq1 = savgol_filter(dq1, window, 3, deriv=1, delta=dt)
    ddq2 = savgol_filter(dq2, window, 3, deriv=1, delta=dt)

    # 构建回归矩阵 Y (2x9)
    sin_q1 = np.sin(q1)
    cos_q1 = np.cos(q1)
    sin_q2 = np.sin(q2)
    cos_q2 = np.cos(q2)
    sin_2q2 = 2 * sin_q2 * cos_q2
    sin2_q2 = sin_q2 ** 2
    cos2_q2 = cos_q2 ** 2
    dq1_dq2 = dq1 * dq2
    dq1_sq = dq1 ** 2

    # 重力: g = [0, 0, -9.81]
    gx, gy, gz = 0.0, 0.0, -9.81
    gx_sin_gy_cos_q1 = gx * sin_q1 - gy * cos_q1
    gx_cos_gy_sin_q1 = gx * cos_q1 + gy * sin_q1

    N = len(q1)
    Y_mat = np.zeros((2 * N, 9))

    # Yaw 轴 (行 0..N-1)
    Y_mat[:N, 0] = sin2_q2 * ddq1 + sin_2q2 * dq1_dq2
    Y_mat[:N, 1] = cos2_q2 * ddq1 - sin_2q2 * dq1_dq2
    Y_mat[:N, 3] = gx_sin_gy_cos_q1 * cos_q2
    Y_mat[:N, 4] = gx_sin_gy_cos_q1 * sin_q2
    Y_mat[:N, 5] = dq1
    Y_mat[:N, 6] = np.tanh(dq1)

    # Pitch 轴 (行 N..2N-1)
    Y_mat[N:, 0] = -0.5 * sin_2q2 * dq1_sq
    Y_mat[N:, 1] = 0.5 * sin_2q2 * dq1_sq
    Y_mat[N:, 2] = ddq2
    Y_mat[N:, 3] = gx_cos_gy_sin_q1 * sin_q2 + gz * cos_q2
    Y_mat[N:, 4] = -gx_cos_gy_sin_q1 * cos_q2 + gz * sin_q2
    Y_mat[N:, 7] = dq2
    Y_mat[N:, 8] = np.tanh(dq2)

    # 预测力矩
    tau_pred = Y_mat @ theta
    tau1_pred = tau_pred[:N]
    tau2_pred = tau_pred[N:]

    # 误差统计
    err1 = tau1 - tau1_pred
    err2 = tau2 - tau2_pred

    rmse1 = np.sqrt(np.mean(err1 ** 2))
    rmse2 = np.sqrt(np.mean(err2 ** 2))
    mae1 = np.mean(np.abs(err1))
    mae2 = np.mean(np.abs(err2))

    print(f"\n  Yaw  跟踪误差: RMSE = {rmse1:.4f} Nm, MAE = {mae1:.4f} Nm")
    print(f"  Pitch 跟踪误差: RMSE = {rmse2:.4f} Nm, MAE = {mae2:.4f} Nm")

    # 各项分解
    print(f"\n  Yaw 力矩分解 (均方根):")
    print(f"    惯量项:  {np.sqrt(np.mean((Y_mat[:N, 0] * theta[0] + Y_mat[:N, 1] * theta[1]) ** 2)):.4f}")
    print(f"    重力项:  {np.sqrt(np.mean((Y_mat[:N, 3] * theta[3] + Y_mat[:N, 4] * theta[4]) ** 2)):.4f}")
    print(f"    摩擦项:  {np.sqrt(np.mean((Y_mat[:N, 5] * theta[5] + Y_mat[:N, 6] * theta[6]) ** 2)):.4f}")

    print(f"\n  Pitch 力矩分解 (均方根):")
    print(f"    耦合项:  {np.sqrt(np.mean((Y_mat[N:, 0] * theta[0] + Y_mat[N:, 1] * theta[1]) ** 2)):.4f}")
    print(f"    惯量项:  {np.sqrt(np.mean((Y_mat[N:, 2] * theta[2]) ** 2)):.4f}")
    print(f"    重力项:  {np.sqrt(np.mean((Y_mat[N:, 3] * theta[3] + Y_mat[N:, 4] * theta[4]) ** 2)):.4f}")
    print(f"    摩擦项:  {np.sqrt(np.mean((Y_mat[N:, 7] * theta[7] + Y_mat[N:, 8] * theta[8]) ** 2)):.4f}")

    return {"rmse_yaw": rmse1, "rmse_pitch": rmse2, "mae_yaw": mae1, "mae_pitch": mae2}


